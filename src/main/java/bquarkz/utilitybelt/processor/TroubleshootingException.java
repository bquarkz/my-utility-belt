package bquarkz.utilitybelt.processor;

@SuppressWarnings( "rawtypes" )
public class TroubleshootingException extends RuntimeException
{
    // ****************************************************************************************
    // Const Fields
    // ****************************************************************************************
    private static final long serialVersionUID = 8085228864014755939L;

    // ****************************************************************************************
    // Common Fields
    // ****************************************************************************************
    private final IProcess process;
    
    // ****************************************************************************************
    // Constructors
    // ****************************************************************************************
    public TroubleshootingException( IProcess process )
    {
        super();
        this.process = process;
    }
    
    public TroubleshootingException( IProcess process, Throwable t )
    {
        super( t );
        this.process = process;
    }

    // ****************************************************************************************
    // Methods
    // ****************************************************************************************

    // ****************************************************************************************
    // Getters And Setters Methods
    // ****************************************************************************************
    public IProcess getProcess()
    {
        return process;
    }

    // ****************************************************************************************
    // Patterns
    // ****************************************************************************************
}
