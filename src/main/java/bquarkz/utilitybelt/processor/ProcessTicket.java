package bquarkz.utilitybelt.processor;

public class ProcessTicket< T >
{
    // ****************************************************************************************
    // Const Fields
    // ****************************************************************************************

    // ****************************************************************************************
    // Common Fields
    // ****************************************************************************************
    private final Long id;

    // ****************************************************************************************
    // Constructors
    // ****************************************************************************************
    protected ProcessTicket( Long id )
    {
        this.id = id;
    }

    // ****************************************************************************************
    // Factories
    // ****************************************************************************************

    // ****************************************************************************************
    // Getters And Setters Methods
    // ****************************************************************************************
    public Long getId()
    {
        return id;
    }

    // ****************************************************************************************
    // Methods
    // ****************************************************************************************
    @Override
    public int hashCode()
    {
        final int prime = 31;
        int result = 1;
        result = prime * result + ( ( id == null ) ? 0 : id.hashCode() );
        return result;
    }

    // ****************************************************************************************
    // Patterns
    // ****************************************************************************************

}
