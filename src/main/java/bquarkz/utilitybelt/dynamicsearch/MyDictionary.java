package bquarkz.utilitybelt.dynamicsearch;

import bquarkz.utilitybelt.readonlymap.ReadOnlyMap;

import java.util.LinkedHashMap;
import java.util.Map;

public class MyDictionary implements Grammar.Dictionary
{
    // ****************************************************************************************************************
    // Const Fields
    // ****************************************************************************************************************
    private static final ReadOnlyMap< String, String > dictionary = createMap();

    // ****************************************************************************************************************
    // Common Fields
    // ****************************************************************************************************************

    // ****************************************************************************************************************
    // Transient
    // ****************************************************************************************************************

    // ****************************************************************************************************************
    // Constructors
    // ****************************************************************************************************************
    protected MyDictionary()
    {
    }

    // ****************************************************************************************************************
    // Factories
    // ****************************************************************************************************************

    // ****************************************************************************************************************
    // Features
    // ****************************************************************************************************************

    // ****************************************************************************************************************
    // Getters And Setters Methods
    // ****************************************************************************************************************
    @Override public ReadOnlyMap< String, String > get()
    {
        return dictionary;
    }

    private static ReadOnlyMap< String, String > createMap()
    {
        Map< String, String > map = new LinkedHashMap<>();
        return ReadOnlyMap.wrap( map );
    }

    // ****************************************************************************************************************
    // Methods
    // ****************************************************************************************************************

    // ****************************************************************************************************************
    // Patterns
    // ****************************************************************************************************************
}