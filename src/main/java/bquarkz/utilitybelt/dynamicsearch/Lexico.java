package bquarkz.utilitybelt.dynamicsearch;

public enum Lexico
{
    // ****************************************************************************************
    // Patterns
    // ****************************************************************************************
    UNDEFINED,
    CONTENT,
    AND_OPERATOR( '&', '&' ),
    OR_OPERATOR( '|', '|' ),
    NOT_OPERATOR( '!', '!' ),
    GROUP_OPEN( '(', '(' ),
    GROUP_CLOSE( ')', ')' ),

    ;

    // ****************************************************************************************
    // Constructors
    // ****************************************************************************************
    Lexico()
    {
        content = null;
    }

    Lexico( char... content )
    {
        this.content = content;
    }

    // ****************************************************************************************
    // Fields & Ks
    // ****************************************************************************************
    private final char[] content;

    // ****************************************************************************************
    // Methods
    // ****************************************************************************************

    // ****************************************************************************************
    // Getters And Setters Methods
    // ****************************************************************************************
    public char[] getContent()
    {
        return content;
    }
}
