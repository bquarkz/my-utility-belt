package bquarkz.utilitybelt.dynamicsearch;

public class Token
{
    // ****************************************************************************************************************
    // Const Fields
    // ****************************************************************************************************************

    // ****************************************************************************************************************
    // Common Fields
    // ****************************************************************************************************************
    private       Lexico        lexico;
    private final StringBuilder sequence;

    // ****************************************************************************************************************
    // Transient
    // ****************************************************************************************************************

    // ****************************************************************************************************************
    // Constructors
    // ****************************************************************************************************************
    Token( Lexico lexico )
    {
        this.lexico = lexico;
        if( lexico.getContent() == null )
        {
            this.sequence = new StringBuilder();
        } else {
            this.sequence = new StringBuilder( String.valueOf( lexico.getContent() ) );
        }
    }

    // ****************************************************************************************************************
    // Factories
    // ****************************************************************************************************************

    // ****************************************************************************************************************
    // Features
    // ****************************************************************************************************************

    // ****************************************************************************************************************
    // Getters And Setters Methods
    // ****************************************************************************************************************
    public boolean isParseable()
    {
        return lexico == Lexico.UNDEFINED;
    }

    public StringBuilder getSequence()
    {
        return sequence;
    }

    public Lexico getLexico()
    {
        return lexico;
    }

    public void setLexico( Lexico lexico )
    {
        this.lexico = lexico;
    }

    public boolean isValidToken()
    {
        return getSequence().length() > 0;
    }

    // ****************************************************************************************************************
    // Methods
    // ****************************************************************************************************************
    @Override public String toString()
    {
        return "Token{" + "lexico=" + lexico +
                ", sequence=" + sequence.toString().trim() +
                ", parseable: " + isParseable() + "}";
    }

    // ****************************************************************************************************************
    // Patterns
    // ****************************************************************************************************************
}